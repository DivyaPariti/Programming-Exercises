# Problem Statement
# Given an integer input , whether the given input is "Positive" or "Negative" or "Zero" and print the corresponding message.

# Input Format
# Accept an integer as input

# Output Format
# Print the output

# Constraints
# NA
n = int(input(""))
if n > 0:
    print("Positive")
elif n == 0:
    print("Zero")
else:
    print("Negative")